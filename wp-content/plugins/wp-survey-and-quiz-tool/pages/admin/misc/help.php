	<h3>Questions</h3>
	
		<a name="create_a_question"></a>
		<h4>How do I create questions?</h4>	
		<p>To create a question <strong>`<em>Quizzes/Surveys</em>`</strong> -&gt; <strong>`<em>Quiz/Survey</em>`</strong> then locate the quiz you wish to add questions too in the table list. Click <strong>`<em>Edit Questions</em>`</strong> then fill out the form. You will then see a list of questions to add a new question click <strong>`<em>Add New Question</em>`</strong> and fill out the form. Questions must be assigned to a section for them to be displayed!</p>
		
		<a name="edit_a_question"></a>
		<h4>How do I edit a question?</h4>	
		<p>To edit a quiz you need to go to <strong>`<em>Quizzes/Surveys</em>`</strong> -&gt; <strong>`<em>Quiz/Survey</em>`</strong> then locate the quiz in the table list. When quiz is located click <strong>`<em>Edit</em>`</strong> you will then see a list of questions locate the question you wish to edit and then click the <strong>`<em>Edit</em>`</strong> and fill out form accordingly.</p>
			
		<a name="delete_a_question"></a>
		<h4>How do I delete a question?</h4>	
		<p>To edit a quiz you need to go to <strong>`<em>Quizzes/Surveys</em>`</strong> -&gt; <strong>`<em>Quiz/Survey</em>`</strong> then locate the quiz in the table list. When quiz is located click <strong>`<em>Edit</em>`</strong> you will then see a list of questions locate the question you wish to edit and then click the <strong>`<em>Delete</em>`</strong> and then confirm.</p>
		
		<a name="how_many_question"></a>
		<h4>How many question can I have?</h4>	
		<p>You can have as many as you would like. If you have issues create new questions please report this to us.</p>
		
