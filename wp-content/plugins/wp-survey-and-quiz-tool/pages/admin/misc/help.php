		<p>HTML is enabled for the <strong>`<em>additional</em>`</strong> field but not the actual question question.</p>
		
	<h3>Marking</h3>
		
		<a name="mark_result"></a>
		<h4>How do I mark a result?</h4>	
		<p>To mark a result, you go to <strong>`<em>Quizzes/Surveys</em>`</strong> -&gt; Find the quiz and hover over and click -&gt; <strong>`<em>Results</em>`</strong> then click <strong>`<em>Mark</em>`</strong>. Then you can then see the answers for multiple choice questions with the correct one in Green text and any ones given that are incorrect in red. For questions as text area type the answers are given in the Grey boxed area with the marking choice below it aswell as an area for comments. There is also a hint area which be shown and hidden.</p>
		
		<a name="automarking"></a>
		<h4>What is auto marking?</h4>	
		<p>Auto marking is when the results are automatically marked upon completion. It can only be done on multiple choice only sections.</p>
		
		<a name="how_do_view_marked"></a>
		<h4>How do I view results that have already been marked?</h4>	
		<p>When a result is marked the <strong>`<em>Mark</em>`</strong> button disappears and is replaced with the score. The score is click-able and will lead you to the marking page where you can update the score and whatnot.</p>

	<h3>Sections</h3>

		<a name="what_are_sections"></a>
		<h4>What are sections?</h4>	
		<p>Sections are pages within the quiz. They each have a different name and the question type is configurable.</p>
		
		<a name="create_a_section"></a>
		<h4>How to create a section?</h4>	
		<p>To create a section you need to go to <strong>`<em>Quizzes/Surveys</em>`</strong> then locate the quiz for which the section belongs to then click <strong>`<em>Sections </em>`</strong> link. Then complete the form and click the <strong>`<em>Save</em>`</strong> button.</p>
		
		<a name="edit_a_section"></a>
		<h4>How to edit a section?</h4>	
		<p>To create a section you need to go to <strong>`<em>Quizzes/Surveys</em>`</strong> then locate the quiz for which the section belongs to then click <strong>`<em>Sections </em>`</strong> link. Then edit form and click the <strong>`<em>Save</em>`</strong> button.</p>
	
		<a name="delete_a_section"></a>
		<h4>How to delete a section?</h4>	
		<p>To delete a section you need to go to <strong>`<em>Quizzes/Surveys</em>`</strong> then locate the quiz for which the section belongs to then click <strong>`<em>Sections </em>`</strong> link. Then empty the section's name and then click the <strong>`<em>Save</em>`</strong> button.</p>
	
		<a name="how_many_sections"></a>
		<h4>How many sections can I have?</h4>
		<p>You can have as many as you would like. If you have issues create new sections please report this to us.</p>
	
		<a name="number_of_questions"></a>
		<h4>A quiz section doesn't always show the correct number of questions whys this?</h4>
		<p>You will have set the difficulty to mixed. If you have set all your questions to the same difficulty please change the section difficulty to match. This is only to be used when there is an even number of questions for each difficulty and is advised to only be used when there are more questions than the question number.</p>
		
	<h3>Customization</h3>
	
		<a name="change_layout"></a>
		<h4>Can I change the quizzes/survey layout/style?</h4>
		<p>
			To customize the layout of survey and quizzes you will have to follow the following steps, this will allow you to create highly customized pages that won't be over written when a new update is released.
			<ol>
				<li>Goto configure quiz and locate the location of the custom pages directory for the quiz, each one is different.</li>
				<li>Goto that directory if it doesn't exist create it.</li>
				<li>Copy the pages/site directory into the custom pages directory.</li>
				<li>Edit files in the custom pages directory.</li>
				<li>Enjoy new custom pages.</li>
			</ol>
		</p>		


