	<h3>Quizes</h3>
	
		<a name="create_a_quiz"></a>
		<h4>How to create a quiz?</h4>	
		<p>To create a quiz you need to go to <strong>`<em>Quizzes/Surveys</em>`</strong> -&gt; then click <strong>`</strong><em>Add New Quiz</em>`</strong> button. Then complete the form and click the <strong>`<em>Save Quiz</em>`</strong> button.</p>
		
		<a name="edit_a_quiz"></a>
		<h4>How to edit a quiz?</h4>	
		<p>To edit a quiz you need to goto <strong>`<em>Quizzes</em>`</strong> then locate the quiz in the table list. When quiz is located click <strong>`<em>Edit</em>`</strong>.</p>
	
		<a name="delete_a_quiz"></a>
		<h4>How to delete a quiz?</h4>	
		<p>To delete a quiz you need to go to <strong>`<em>Quizzes</em>`</strong> then locate the quiz in the table list. When quiz is located click <strong>`<em>Delete</em>`</strong>.</p>
	
		<a name="how_many_quiz"></a>
		<h4>How many quizzes can I have?</h4>	
		<p>You can have as many as you would like. If you have issues create new quizzes please report this to us.</p>
	
		<a name="display_quiz"></a>
		<h4>How do I display a quiz on a page?</h4>
		<p>To display a quiz on a page you need to use the shortcode, [wpsqt_quiz name=""] in put in the name of the quiz you want to display in between the quote marks. You can do this in a post or in a page, a new seperate page is advised. <em>Note you may want to disable comments.</em></p>
	
